    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String nomePessoa = campoNome.getText();
        String idadePessoa = campoIdade.getText();
        String pesoPessoa = campoPeso.getText();
        String alturaPessoa = campoAltura.getText();
        if(nomePessoa.isEmpty() || idadePessoa.isEmpty() || pesoPessoa.isEmpty() || alturaPessoa.isEmpty()) {
            campoResposta.setText("Por favor preencha todas as informações");
        }         
        else {
            int idade = Integer.parseInt(idadePessoa);
            double peso = Double.parseDouble(pesoPessoa);
            double altura = Double.parseDouble(alturaPessoa);
            Pessoa p = new Pessoa(nomePessoa, idade, peso, altura);
            Imc imc = new Imc();
            imc.calculaImc(p.getPeso(), p.getAltura());
            imc.classificaImc();
            p.setImc(imc);
            pessoas.add(p);
            atualizaTela();
            campoNome.setText("");
            campoIdade.setText("");
            campoPeso.setText("");
            campoAltura.setText("");
            campoResposta.setText("IMC calculado com sucesso");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        for(int i=0; i < pessoas.size(); i++) {
            Pessoa p = pessoas.get(i);
            String nomePessoa = campoNome.getText();
            if(p.getNome().equals(nomePessoa)) {
                pessoas.remove(p);
                campoNome.setText("");
            }
            atualizaTela();
            campoResposta.setText("Pessoa removida com sucesso");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

