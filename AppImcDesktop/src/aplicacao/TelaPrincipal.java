    public void atualizaTela() {
        Object [][] model = new Object[pessoas.size()][6];
        for(int i=0; i < pessoas.size(); i++) {
            Pessoa p = pessoas.get(i);
            model[i][0] = p.getNome();
            model[i][1] = p.getIdade();
            model[i][2] = p.getPeso();
            model[i][3] = p.getAltura();
            model[i][4] = p.getImc().getImc();
            model[i][5] = p.getImc().getClassificacaoImc();
        }
        jTable1.setModel(new javax.swing.table.DefaultTableModel(
            model,
            new String [] {
                "Nome", "Idade", "Peso", "Altura", "IMC", "Índice"
            }
        ));
    }

