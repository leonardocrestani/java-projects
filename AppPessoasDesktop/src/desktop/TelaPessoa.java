    private void atualizarTela() {
        // pegar colecao de objetos e converter em uma matriz para a colocar na tabela
        Object [][] model = new Object[pessoas.size()][2];
        for(int i=0; i < pessoas.size(); i++) {
            Pessoa p = pessoas.get(i);
            model[i][0] = p.getNome();
            model[i][1] = p.getIdade();
        }
        
        tabelaPessoas.setModel(new javax.swing.table.DefaultTableModel(
            model,
            new String [] {
                "Nome", "Idade"
            }
        ));
    }
