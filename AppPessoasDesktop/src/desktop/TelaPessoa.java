    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        String nomePessoa = campoNome.getText();
        String idadePessoa = campoIdade.getText();
        if(nomePessoa.isEmpty() || idadePessoa.isEmpty()) {
            campoResposta.setText("Não é possível adicionar pessoa sem nome ou idade");
        }
        else {
            // Instanciar uma pessoa e pegar os dados do formulario
            int idade = Integer.parseInt(idadePessoa);
            Pessoa p = new Pessoa(nomePessoa, idade);
            pessoas.add(p); // Coloca a pessoa criada no vetor
            campoNome.setText("");
            campoIdade.setText("");
            // Atualizar tabela/tela
            atualizarTela();
            campoResposta.setText("Pessoa adicionada com sucesso");
        }
    }//GEN-LAST:event_botaoSalvarActionPerformed

    private void botaoRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRemoverActionPerformed
        for(int i=0; i < pessoas.size(); i++) {
            Pessoa p = pessoas.get(i);
            // Verifica quando o nome de algum objeto do array for igual ao nome que deseja excluir
            String nomeRemover = campoNome.getText();
            if(p.getNome().equals(nomeRemover)) {
                // Remove o objeto do array
                pessoas.remove(p);
                campoNome.setText("");
                campoIdade.setText("");
            }
            atualizarTela();
            campoResposta.setText("Pessoa removida com sucesso");
        }
    }//GEN-LAST:event_botaoRemoverActionPerformed

